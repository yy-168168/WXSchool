package com.wxschool.entity;

public class Menber {

	private int menberId;
	private int menberNum;
	private String menberName;
	private String tel;
	private String wxaccount;
	private String userwx;
	private int status;

	public int getMenberId() {
		return menberId;
	}

	public void setMenberId(int menberId) {
		this.menberId = menberId;
	}

	public int getMenberNum() {
		return menberNum;
	}

	public void setMenberNum(int menberNum) {
		this.menberNum = menberNum;
	}

	public String getMenberName() {
		return menberName;
	}

	public void setMenberName(String menberName) {
		this.menberName = menberName;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getWxaccount() {
		return wxaccount;
	}

	public void setWxaccount(String wxaccount) {
		this.wxaccount = wxaccount;
	}

	public String getUserwx() {
		return userwx;
	}

	public void setUserwx(String userwx) {
		this.userwx = userwx;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
}
