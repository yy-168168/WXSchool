package com.wxschool.entity;

public class WdUser {

	private int uid;
	private String name;
	private String password;
	private String token;
	private String wdaccount;
	private String wdpwd;
	private String wduserid;
	private String wduss;
	private String pubTime;
	private String uptTime;
	private int status;

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public String getWdaccount() {
		return wdaccount;
	}

	public void setWdaccount(String wdaccount) {
		this.wdaccount = wdaccount;
	}

	public String getWdpwd() {
		return wdpwd;
	}

	public void setWdpwd(String wdpwd) {
		this.wdpwd = wdpwd;
	}

	public String getWduserid() {
		return wduserid;
	}

	public void setWduserid(String wduserid) {
		this.wduserid = wduserid;
	}

	public String getWduss() {
		return wduss;
	}

	public void setWduss(String wduss) {
		this.wduss = wduss;
	}

	public String getPubTime() {
		return pubTime;
	}

	public void setPubTime(String pubTime) {
		this.pubTime = pubTime;
	}

	public String getUptTime() {
		return uptTime;
	}

	public void setUptTime(String uptTime) {
		this.uptTime = uptTime;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
}
